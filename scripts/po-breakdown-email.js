const { google } = require('googleapis');
const nodemailer = require('nodemailer');

// Configuration
const SHEET_ID = '1y0iL7PJldbVQmPIAnJi9wvA2hvjB8_aK2bU2kxvUf5Q';
const GID = '0'; // Production & PO DataBase sheet

// Email configuration
const EMAIL_CONFIG = {
  service: 'gmail',
  auth: {
    user: process.env.EMAIL_USER,
    pass: process.env.EMAIL_PASS
  }
};

const RECIPIENT_EMAIL = 'susan@creativetwotwelve.com';

async function getSheetData() {
  const auth = new google.auth.GoogleAuth({
    credentials: JSON.parse(process.env.GOOGLE_SERVICE_ACCOUNT_JSON),
    scopes: ['https://www.googleapis.com/auth/spreadsheets.readonly']
  });

  const sheets = google.sheets({ version: 'v4', auth });
  const response = await sheets.spreadsheets.values.get({
    spreadsheetId: SHEET_ID,
    range: `Production & PO DataBase!A:Z` // Adjust range as needed
  });

  return response.data.values;
}

function findPOData(data, styleNumber) {
  const headers = data[0].map(h => h.toString().trim().toLowerCase());

  const getColIndex = (targetName) => {
    const normalized = targetName.trim().toLowerCase();
    return headers.findIndex(h => h === normalized);
  };

  const colMap = {
    style: getColIndex("style #"),
    status: getColIndex("status"),
    supplier: getColIndex("supplier"),
    category: getColIndex("category"),
    subcategory: getColIndex("sub-category"),
    piReceived: getColIndex("buyer presentation"), // Closest match
    topSent: getColIndex("sms sent to anthro"), // SMS SENT TO ANTHRO
    topSampleStatus: getColIndex("top approval from anthro"), // TOP APPROVAL FROM ANTHRO
    basePo: getColIndex("po info anthro"), // PO INFO ANTHRO
    finalNDC: getColIndex("final ndc"),
    topDeadline: getColIndex("sms deadline"), // SMS deadline
    exFactory: getColIndex("ex factory / flight date"),
    cost: getColIndex("cost"),
    freight: getColIndex("freight"),
    duty: getColIndex("duty"),
    hts: getColIndex("hts code")
  };

  for (let i = 1; i < data.length; i++) {
    const row = data[i];
    const style = row[colMap.style]?.toString().trim();
    const status = row[colMap.status]?.toString().trim();

    if (style === styleNumber && (status === "PO'd + production ok" || status === "PO'd")) {
      const finalNDC = row[colMap.finalNDC];
      const topDeadline = row[colMap.topDeadline];

      let topDeadlineDays = '';
      if (finalNDC && topDeadline) {
        try {
          const finalNDCDate = new Date(finalNDC);
          const topDeadlineDate = new Date(topDeadline);
          if (!isNaN(finalNDCDate) && !isNaN(topDeadlineDate)) {
            const diffDays = Math.round((finalNDCDate - topDeadlineDate) / (1000 * 60 * 60 * 24));
            topDeadlineDays = `${diffDays} days`;
          }
        } catch (e) {
          // Ignore date parsing errors
        }
      }

      return {
        style: row[colMap.style] || '',
        supplier: row[colMap.supplier] || '',
        category: row[colMap.category] || '',
        subcategory: row[colMap.subcategory] || '',
        piReceived: row[colMap.piReceived] || '',
        topSent: row[colMap.topSent] || '',
        topSampleStatus: row[colMap.topSampleStatus] || '',
        basePo: row[colMap.basePo] || '',
        finalNDC: row[colMap.finalNDC] || '',
        topDeadline: row[colMap.topDeadline] || '',
        topDeadlineDays: topDeadlineDays,
        exFactory: row[colMap.exFactory] || '',
        cost: row[colMap.cost] || '',
        freight: row[colMap.freight] || '',
        duty: row[colMap.duty] || '',
        hts: row[colMap.hts] || ''
      };
    }
  }

  return null;
}

async function sendBreakdownEmail(poData) {
  const transporter = nodemailer.createTransporter(EMAIL_CONFIG);

  const emailContent = `
PO BREAKDOWN - Style #${poData.style}

SUPPLIER: ${poData.supplier}
CATEGORY: ${poData.category}
SUB-CATEGORY: ${poData.subcategory}

PI RECEIVED: ${poData.piReceived}
TOP SENT TO ANTHRO: ${poData.topSent}
TOP SAMPLE STATUS: ${poData.topSampleStatus}
BASE PO IMPORT FARM: ${poData.basePo}

FINAL NDC: ${poData.finalNDC}
TOP DEADLINE: ${poData.topDeadline}
TOP DEADLINE DAYS: ${poData.topDeadlineDays}

EX FACTORY / FLIGHT DATE: ${poData.exFactory}
COST: ${poData.cost}
FREIGHT: ${poData.freight}
DUTY: ${poData.duty}
HTS: ${poData.hts}

---
Generated by Susan Agent
${new Date().toLocaleString()}
  `.trim();

  await transporter.sendMail({
    from: `"Susan Agent" <${process.env.EMAIL_USER}>`,
    to: RECIPIENT_EMAIL,
    subject: `PO BREAKDOWN - Style #${poData.style}`,
    text: emailContent,
    html: emailContent.replace(/\n/g, '<br>')
  });

  console.log(`PO Breakdown email sent for Style #${poData.style}`);
}

async function main() {
  try {
    // Get style number from command line argument or environment
    const styleNumber = process.argv[2] || process.env.STYLE_NUMBER;
    if (!styleNumber) {
      console.error('Please provide a style number as argument or set STYLE_NUMBER environment variable');
      process.exit(1);
    }

    console.log(`Searching for PO data for Style #${styleNumber}...`);

    const sheetData = await getSheetData();
    const poData = findPOData(sheetData, styleNumber.trim());

    if (!poData) {
      console.error(`Style #${styleNumber} not found or status is not valid for PO breakdown`);
      process.exit(1);
    }

    console.log('PO data found, sending breakdown email...');
    await sendBreakdownEmail(poData);
    console.log('✅ PO Breakdown email sent successfully');

  } catch (error) {
    console.error('Error:', error.message);
    process.exit(1);
  }
}

if (require.main === module) {
  main();
}

module.exports = { findPOData, sendBreakdownEmail };